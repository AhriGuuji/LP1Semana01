using System;

namespace VariousTypes
{
    public class Program
    {
        private static void Main(string[] args)
        {
            int number = 8;
            int another = 9;
            int inteiro = 5;
            Console.WriteLine($"{number}\n{another}\n{inteiro}");
            char music = '♫';
            char pencil = '✎';
            char mail = '✉';
            Console.WriteLine($"{music}\n{pencil}\n{mail}");
            float slice = 0.2f;
            float quantityOfApples = 3.3f;
            float aPartOfSomething = 0.4f;
            Console.WriteLine($"{slice}\n{quantityOfApples}\n{aPartOfSomething}");
            bool verdade = true;
            bool falso = false;
            Console.WriteLine($"{verdade}\n{falso}");
        }
    }
}
